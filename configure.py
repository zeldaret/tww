# Helper function for single-object RELs
def Rel(status, rel_name, cpp_name):
    return {
        "lib": rel_name,
        "mw_version": "GC/1.3.2",
        "cflags": cflags_rel,
        "host": True,
        "objects": [
            Object(status, cpp_name),
        ],
    }


# Helper function for actor RELs
def ActorRel(status, rel_name):
    return Rel(status, rel_name, f"d/actor/{rel_name}.cpp")


