// Particle effect name enums.
// "dPa_name::ID_SCENE_END" is official and appears in debug assertion strings.
// dPa_name::ID_NONE is unofficial.
// All other names are derived from official name strings from TWWHD, e.g.:
// "ak_jn_o_fire00" => dPa_name::ID_AK_JN_O_FIRE00
// The first two letters are an abbreviation of the author's name (e.g. "ak", "it").
// The third letter is where the particle should be loaded from:
//   j - Particle in the always-loaded particle bank common.jpc.
//   s - Particle in a scene-specific particle bank (e.g. Pscene051.jpc). Corresponds to bit 0x8000 in the particle ID.
// The fourth letter is the type of particle:
//   n - Normal.
//   t - Toon. Corresponds to bit 0x2000 in the particle ID.
//   p - Projection. Corresponds to bit 0x4000 in the particle ID.
//   1, 2, 4 - Unknown.
// The remaining characters are the name of the specific particle effect.
// Some of these names are duplicates, e.g. ID_AK_SN_BOSSWARPWIND00 is both 0x81EB and 0x820A.
// These duplicates are commented out for now.
// If any are actually used they should be uncommented and given a unique name to distinguish them (e.g. ID_AK_SN_BOSSWARPWIND00_81EB).
